/****************************************************************
 * gpio_set_dir
 ****************************************************************/
int gpio_set_dir(unsigned int gpio, unsigned int out_flag)
{
	int fd, len;
	char buf[MAX_BUF];
 
	len = snprintf(buf, sizeof(buf), SYSFS_GPIO_DIR  "/gpio%d/direction", gpio);
 
	fd = open(buf, O_WRONLY);
	if (fd < 0) {
		perror("gpio/direction");
		return fd;
	}
 
	if (out_flag)
		write(fd, "out", 4);
	else
		write(fd, "in", 3);
 
	close(fd);
	return 0;
}

/****************************************************************
 * gpio_set_value
 ****************************************************************/
int gpio_set_value(unsigned int gpio, unsigned int value)
{
	int fd, len;
	char buf[MAX_BUF];
 
	len = snprintf(buf, sizeof(buf), SYSFS_GPIO_DIR "/gpio%d/value", gpio);
 
	fd = open(buf, O_WRONLY);
	if (fd < 0) {
		perror("gpio/set-value");
		return fd;
	}
 
	if (value)
		write(fd, "1", 2);
	else
		write(fd, "0", 2);
 
	close(fd);
	return 0;
}

/****************************************************************
 * gpio_get_value
 ****************************************************************/
int gpio_get_value(unsigned int gpio, unsigned int *value)
{
	int fd, len;
	char buf[MAX_BUF];
	char ch;

	len = snprintf(buf, sizeof(buf), SYSFS_GPIO_DIR "/gpio%d/value", gpio);
 
	fd = open(buf, O_RDONLY);
	if (fd < 0) {
		perror("gpio/get-value");
		return fd;
	}
 
	read(fd, &ch, 1);

	if (ch != '0') {
		*value = 1;
	} else {
		*value = 0;
	}
 
	close(fd);
	return 0;
}


/****************************************************************
 * gpio_set_edge
 ****************************************************************/

int gpio_set_edge(unsigned int gpio, char *edge)
{
	int fd, len;
	char buf[MAX_BUF];

	len = snprintf(buf, sizeof(buf), SYSFS_GPIO_DIR "/gpio%d/edge", gpio);
 
	fd = open(buf, O_WRONLY);
	if (fd < 0) {
		perror("gpio/set-edge");
		return fd;
	}
 
	write(fd, edge, strlen(edge) + 1); 
	close(fd);
	return 0;
}

/****************************************************************
 * gpio_fd_open
 ****************************************************************/

int gpio_fd_open(unsigned int gpio)
{
	int fd, len;
	char buf[MAX_BUF];

	len = snprintf(buf, sizeof(buf), SYSFS_GPIO_DIR "/gpio%d/value", gpio);
 
	fd = open(buf, O_RDONLY | O_NONBLOCK);
	if (fd < 0) {
		perror("gpio/fd_open");
	}
	return fd;
}

/****************************************************************
 * gpio_fd_close
 ****************************************************************/

int gpio_fd_close(int fd)
{
	return close(fd);
}

/******************** wrapper functions! ***********************/

/****************************************************************
 * gpio_OPEN(unsigned int gpio) 								*
 * args: 	gpio == gpio id to open								*
 * ret: 	the fd of the opened gpio							*
 ****************************************************************/

int gpio_OPEN(unsigned int gpio) {
  	fprintf(stderr, "SETTING UP DEMO GPIO %d!\n", gpio);

  	// export gpio
  	gpio_export(gpio);
  	// set direction
  	gpio_set_dir(gpio, 0);
  	// set edge
  	gpio_set_edge(gpio, "rising");

  	return gpio_fd_open(gpio);
}

 /***************************************************************
 * gpio_CLOSE(unsigned int gpio_fd) 							*
 * args: 	gpio_fd == gpio file descriptor to close			*
 * ret: 	1 if successful, -1 if failure						*
 ****************************************************************/

 int gpio_CLOSE(unsigned int gpio_fd) {
   	fprintf(stderr, "CLOSING DEMO GPIO (FD = %d)!\n", gpio_fd);
   	return gpio_fd_close(gpio_fd);
 }

 /***************************************************************
 * gpio_GETVAL(unsigned int gpio_fd)							*
 * args: 	gpio_fd == gpio file descriptor to read from		*
 * ret: 	value of the gpio 									*
 ****************************************************************/
int gpio_GETVAL(unsigned int gpio) {
   int retval = -1;
   gpio_get_value(gpio, &retval);
   return retval;
}